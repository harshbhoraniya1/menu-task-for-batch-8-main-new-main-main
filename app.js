let carts = []

let pagesize = 9;
function button(){
  let pages = menu.length/pagesize;

  for(let i = 1;i<=pages;i++){
    let s = (i-1)*pagesize;
    let e = s + pagesize;
    document.getElementById("btns").innerHTML += `<button type="button" class="filter-btn" data-id="all"  onclick="p(menu,${s},${e})">${i}</button>`
  }
}
button();
function p(data,s,e) {
  const newmenu = data.slice(s,e).map(function (v,index) {

    return `<article class="menu-item">
    <img src="${v.img}" alt="menu item" class="photo" />
    <div class="item-info">
      <header>
        <h4>${v.title}</h4>
        <h4 class="price">${v.price}</h4>
      </header>
      <p class="item-text">
       ${v.desc}
       <button type="button" class="filter-btn" data-id="all" onclick="addToCart(${index})">Add To Cart</button>
      </p>
    </div>
  </article>`
  }) 
  console.log(carts);
  document.getElementById("menu").innerHTML = newmenu.join("");
}

p(menu,0,pagesize);

let a = "all"

const q = menu.reduce(function(pre,val){
if (pre.includes(val.category) == false){
  pre.push(val.category)
}
return pre
},[]) 
q.unshift(a);
let categorybtn = q.map(function (v) {
  return `<button type="button" class="filter-btn" data-id="all" onclick="filterData('${v}')">${v}</button>`
})

document.getElementById("btn").innerHTML = categorybtn.join("")

function filterData(category) {
  let filterData = menu.filter(function (v) {
    return v.category == category || category == a;

  })
  p(filterData);
}
function low() {
  menu.sort(function (a, b) {
    return a.price - b.price
  })
  p(menu)
}
function high() {
  menu.sort(function (a, b) {
    return b.price - a.price
  })
  p(menu)
}
function atoz() {
  menu.sort(function (a, b) {
    if (b.title > a.title) {
      return -1;
    }
  })
  p(menu)
}
function ztoa() {
  menu.sort(function (a, b) {
    if (a.title > b.title) {
      return -1;
    }
  })
  p(menu)

}

const addToCart = (index) => {
  let selecteditem = menu[index]

  let exit = carts.find((v)=>{
    return v.id == selecteditem.id;
  })

  if(exit == undefined){
    selecteditem.qty = 1;
    carts.push(selecteditem);
  }
  else{
    selecteditem.qty = selecteditem.qty+1;
  }
  console.log(carts);
  document.querySelector(".dis").innerHTML = carts.length;

}

